<p>
Sus datos personales le corresponden solo a usted y <strong>TAR</strong> es responsable de no revelar ninguna clase de información que le pertenezca (como dirección de correo electrónico, nombre, dirección IP, etcétera), salvo su expresa autorización o fuerzas de naturaleza mayor de tipo legal que así lo requieran.
</p>

<h3>Seguridad de su información personal:</h3>

<p>
<strong>TAR</strong> se hace responsable de velar por su seguridad, por la privacidad 
de su información y por el respeto a sus datos, de acuerdo con las limitaciones 
que la actual Internet nos provee, siendo conscientes que no estamos excluídos 
de sufrir algún ataque por parte de usuarios malintencionados 
que ejerzan la delincuencia informática.
</p>
<h3>Obtención de su información:</h3>
<p>
Todos sus datos personales consignados en este sitio son suministrados por usted mismo, 
haciendo uso entero de su libertad. La información aqui almacenada solo comprende 
datos básicos ingresados mediante formularios de contacto, y servicios que usted
explícitamente concede al acceder al servicio.
</p>

<h3>Uso de la información:</h3>
<p>
Al proporcionarnos sus datos personales, estando de acuerdo con la 
Política de Privacidad aquí consignada, nos autoriza para el siguiente uso de su información: 
<ul>
   <li>a) para el fin mismo por lo cual se ha suministrado; </li>
   <li>b) para considerarlo dentro de nuestras estadísticas de tráfico, incrementando así nuestra oferta publicitaria y de mercado; </li>
   <li>c) para orientar mejor los servicios aquí ofrecidos y valorarlos a su criterio, y </li>
   <li>d) para enviar correo con nuestros boletines, responder inquietudes o comentarios, y mantener informado a nuestros usuarios.</li>
</ul>
</p>
