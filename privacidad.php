<h2>Política de privacidad</h2>

<p>
En tar.mx, en adelante <strong>TAR</strong>, respetamos su información personal y en vista de cumplir con las políticas de seguridad respectivas concernientes a todo sitio web, que deberían ser obligatorias, informo a ustedes lo siguiente.
